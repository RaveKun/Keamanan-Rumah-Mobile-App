package com.keamanan_rumah.sistemkeamananrumah;

public class Pengguna {
    public String username;
    public String nama;
    public String status;

    public Pengguna(){
        super();
    }

    public Pengguna(String nama, String username, String status){
        super();
        this.username = username;
        this.nama = nama;
        this.status = status;
    }
}


